<article>
  <h2>Rose Forgrove Chess Club Privacy Notice</h2>
  <p>
    General Data Protection Regulation (GDPR), Privacy Notice for your
    information.
  </p>

  <h4>What personal data does Rose Forgrove Chess Club collect?</h4>
  <p>
    The data we routinely collect includes members’ names, addresses, email
    addresses and phone numbers. We collect this data directly from our members
    when they join the club and on a yearly basis before the start of the new
    chess season.
  </p>
  <p>
    We collect the scores from the games you play, which are then displayed on
    the Chessnuts website, the Leeds Chess Association (LCA) website, the
    English Chess Federation (ECF) website, on our website and some are
    circulated to members’ through email.
  </p>

  <h4>What is this personal data used for?</h4>
  <p>
    We use members’ data for the administration of your membership, the
    communication of information and the organisation of events.
  </p>

  <h4>Who is your data shared with?</h4>
  <p>
    Members’ renewing their ECF membership through Rose Forgrove Chess Club will
    have the relevant data passed on to the ECF.
  </p>
  <p>
    The scores from the matches you play for the club are passed on to the LCA,
    the Yorkshire Chess Association (YCA) and ECF for calculating your grade.
  </p>
  <p>
    Your personal data is not passed on by us to organisations other than those
    indicated above, whether or not connected with chess.
  </p>

  <h4>Where does this data come from?</h4>
  <p>
    Data for our members comes from them when they join Rose Forgrove Chess
    Club, when they inform the Club of any changes and on a yearly basis before
    the start of the chess season. Scoring data comes directly from the results
    of the games you play.
  </p>

  <h4>How is your data stored?</h4>
  <p>
    This information is stored in digital form on the Club Vice-Captain’s
    computer and in the form of written documents stored at the Club
    Vice-Captain’s home.
  </p>

  <h4>
    Who is responsible for ensuring compliance with the relevant laws and
    regulations?
  </h4>
  <p>
    Under the GDPR we do not have a statutory requirement to have a Data
    Protection Officer. The Rose Forgrove Chess Club Committee is responsible
    for ensuring that the Rose Forgrove Chess Club discharges its obligations
    under the GDPR.
  </p>

  <h4>Who has access to your data?</h4>
  <p>
    Members of the committee of Rose Forgrove Chess Club have access to members’
    data in order for them to carry out their legitimate tasks for the club.
  </p>

  <h4>What is the legal basis for collecting this data?</h4>
  <p>
    Rose Forgrove Chess Club collects personal data that is necessary for the
    purposes of its legitimate interests as a membership organisation and
    participant in an internationally recognised and regulated mind sport.
  </p>
  <p>
    For some data, such as that relating to financial matters, the basis for its
    collection and retention is to comply with our legal obligations.
  </p>

  <h4>How you can check what data we have about you?</h4>
  <p>
    If you want to know the basic membership data we hold about you, you should
    contact Clive Davies.
  </p>

  <h4>Does Rose Forgrove Chess Club collect any “special” data?</h4>
  <p>
    The GDPR refers to sensitive personal data as “special categories of
    personal data”. We do not record any such special data.
  </p>

  <h4>How can you ask for data to be removed, limited, or corrected?</h4>
  <p>There are ways in which you can limit how your data is used.</p>
  <p>
    You could maintain your club membership with your correct name but with
    limited contact details. However, we do need to have at least one method of
    contacting you. You could for example simply maintain an up-to-date email
    address. This option can be implemented by contacting Clive Davies.
  </p>
  <p>
    You do not need to provide us with your date of birth unless you wish to
    gain concessions based on age.
  </p>

  <h4>How long do we keep your data for and why?</h4>
  <p>
    We normally keep members’ data after they resign or their membership lapses
    in case they later wish to re-join. However, we will delete any former
    member’s contact details entirely on request.
  </p>
  <p>
    Since underlying statistical data, like scores from chess games continues to
    be necessary in relation to the purpose for which it was originally
    collected and processed, results from events used for the calculation of
    grades, are not deleted by Rose Forgrove Chess Club, the LCA, the YCA, or
    the ECF.
  </p>
  <p>
    Historical ranking lists and prize lists are required for archiving purposes
    and names cannot be removed from them.
  </p>
  <p>
    Other data, such as that relating to accounting or personnel matters, is
    kept for the legally required period.
  </p>

  <h4>What happens if a member dies?</h4>
  <p>
    We normally keep members’ information after they die. If requested by their
    next-of-kin to delete the information we will do so on the same basis as
    when requested to remove data by a former member.
  </p>
</article>

